SELECT Symptom, COUNT(Symptom) 
FROM Self_Report  GROUP BY Symptom 
HAVING COUNT (Symptom)=( 
SELECT MAX(mycount) 
FROM ( 
SELECT Symptom, COUNT(Symptom) mycount 
FROM Self_Report 
GROUP BY Symptom));